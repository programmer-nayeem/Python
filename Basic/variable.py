### Variable Types in Python

# String example !
first_name = "Mikasa"
last_name = "Ackerman"
full_name = first_name + " " + last_name
print(f"Hello, {full_name}!")

# Integer example !
age = 18
print(f"Hello, {full_name}! You are {age} years old.")

# Float example !
height = 1.75
print(f"Hello, {full_name}! You are {age} years old and {height}m tall.")

# Boolean example !
is_student = True
print(f"Hello, {full_name}! You are {age} years old and {height}m tall.")
if is_student:
    print(f"Hello, {full_name}! You are a student.")
else:
    print(f"Hello, {full_name}! You are not a student.")
# List example !
hobbies = ["reading", "gaming", "cooking"]
print(f"Hello, {full_name}! Your hobbies are: {', '.join(hobbies)}.")


