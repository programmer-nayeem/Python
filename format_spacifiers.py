### Format specifiers for various data types
# This code snippet demonstrates how to use format specifiers in Python.

price1 = 3.14159
price2 = -987.65
price3 = 12.34

# Using format specifiers to format prices
print(f"Price 1 is ${price1:.2f}")
print(f"Price 2 is ${price2:.2f}")
print(f"Price 3 is ${price3:.2f}")
